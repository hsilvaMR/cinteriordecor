<script>
function newsletter()
{
	var email = document.getElementById("mail_news").value;
	if(email){
		$.post("_newsletter/js_newsletter.php",{ email:email })
		.done(function(data){
	   		var jsonRetorna = $.parseJSON(data);
			if(jsonRetorna == 'TM'){
				document.getElementById("mail_news").value = "";
				document.getElementById("bt_news").value="OBRIGADO";
				setTimeout(function(){ document.getElementById("bt_news").value="SUBSCREVER"; }, 2000);
			}
			else{
				document.getElementById("bt_news").value="INVALIDO";
				setTimeout(function(){ document.getElementById("bt_news").value="SUBSCREVER"; }, 2000);
			}
		});
	}
}
